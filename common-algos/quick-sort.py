def quicksort(array):
    if len(array) < 2:
        return array
    pivot = array[0]

    less = [i for i in array[1:] if i<=pivot]
    greater = [j for j in array[1:] if j>pivot]

    return quicksort(less) +[pivot] + quicksort(greater)

